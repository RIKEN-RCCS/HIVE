int VolumeToMeshData::Create(BufferVolumeData *volume) {
    if (!volume)
        return 0;

    if (volume->Component() != 1) {
        fprintf(stderr, "[VolumeToMeshData] The number of component of voxel must be 1. Only see the first component of voxel data.\n");
    }

    // Store pointer reference.
    m_volume = volume;

    return 1; // @fixme
}

int VolumeToMeshData::IsoSurface() {
    if (!m_volume)
        return 0;

    if (m_volume->Component() != 1) {
        fprintf(stderr, "[VolumeToMeshData] The number of component of voxel must be 1. Only see the first component of voxel data.\n");
		printf("Build failed\n");
    }
    
    const float *source  = static_cast<const float*>(m_volume->Buffer()->GetBuffer());
    int dim[3];
    dim[0] = m_volume->Width();
    dim[1] = m_volume->Height();
    dim[2] = m_volume->Depth();
    printf("[VolumeToMeshData] Create marching cube  isosurface value = %f\n", m_isovalue);
    printf("[VolumeToMeshData] dim[0] = %d\n", dim[0]);
    printf("[VolumeToMeshData] dim[1] = %d\n", dim[1]);
    printf("[VolumeToMeshData] dim[2] = %d\n", dim[2]);

    marching_cubes_generator<float> generator(source, dim, m_volume->Component());
    generator.generate(m_isovalue);

    const std::vector<float>& vertices = generator.get_vertices();
    const std::vector<float>& normals = generator.get_normals();
    const std::vector<size_t>& indices = generator.get_indices();

    delete m_mesh;
    m_mesh  = new BufferMeshData();

    size_t numVertices = vertices.size() / 3;
    size_t numIndices  = indices.size();

    if (numIndices < 3 || numVertices < 3) {
        printf("[VolumeToMeshData] Index or Vertex invalid...\n");
        return 0;
    }

    m_mesh->Create(numVertices, numIndices);

    Vec3Buffer* pos      = m_mesh->Position();
    Vec3Buffer* normal   = m_mesh->Normal();
    //FloatBuffer* mat     = m_mesh->Material(); // @todo
    UintBuffer* index    = m_mesh->Index();
    //Vec2Buffer* texcoord = m_mesh->Texcoord(); // @todo
    
    pos->Create(numVertices);
    float* pp = pos->GetBuffer();
    memcpy(pp, &vertices.at(0), sizeof(float)*3*numVertices);

    normal->Create(numVertices);
    memcpy(normal->GetBuffer(), &normals.at(0), sizeof(float)*3*numVertices);

    index->Create(numIndices);
    unsigned int* idxDst = index->GetBuffer();
    for (size_t i = 0; i < numIndices; i++) {
        idxDst[i] = static_cast<unsigned int>(indices[i]);
    }
    printf("[VolumeToMeshData] numVertices = %d\n", numVertices);

    return numVertices;
}


