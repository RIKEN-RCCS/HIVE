struct index_pair {
  index_pair(size_t a, size_t b) : a_(a), b_(b) {
    if (a_ > b_)
      std::swap(a_, b_);
  }
  bool less(const index_pair &rhs) const {
    if (a_ == rhs.a_) {
      return b_ < rhs.b_;
    } else {
      return a_ < rhs.a_;
    }
  }
  size_t a_;
  size_t b_;
};
inline bool operator<(const index_pair &lhs, const index_pair &rhs) {
  return lhs.less(rhs);
}

struct float3 {
  float3() {}
  float3(float xx, float yy, float zz) {
    x = xx;
    y = yy;
    z = zz;
  }

  float3 operator*(float f) const { return float3(x * f, y * f, z * f); }
  float3 operator-(const float3 &f2) const {
    return float3(x - f2.x, y - f2.y, z - f2.z);
  }
  float3 operator*(const float3 &f2) const {
    return float3(x * f2.x, y * f2.y, z * f2.z);
  }
  float3 operator+(const float3 &f2) const {
    return float3(x + f2.x, y + f2.y, z + f2.z);
  }
  float3 operator/(const float3 &f2) const {
    return float3(x / f2.x, y / f2.y, z / f2.z);
  }
  float operator[](int i) const { return (&x)[i]; }
  float &operator[](int i) { return (&x)[i]; }

  float length() { return sqrt(x * x + y * y + z * z); }

  void normalize() {
    float len = length();
    if (fabs(len) > 1.0e-6) {
      float inv_len = 1.0 / len;
      x *= inv_len;
      y *= inv_len;
      z *= inv_len;
    }
  }

  float x, y, z;
  float pad; // match padding/alignment of ispc version
};

inline float3 vcross(float3 a, float3 b) {
  float3 c;
  c[0] = a[1] * b[2] - a[2] * b[1];
  c[1] = a[2] * b[0] - a[0] * b[2];
  c[2] = a[0] * b[1] - a[1] * b[0];
  return c;
}

void compute_smooth_normals(std::vector<float> &normals,
                            const std::vector<float> &vertices,
                            const std::vector<size_t> &indices) {

  size_t vsz = vertices.size() / 3;
  size_t fsz = indices.size() / 3;

  normals.resize(vertices.size());

  for (size_t i = 0; i < normals.size(); i++) {
    normals[i] = 0.0f;
  }

  for (size_t i = 0; i < fsz; i++) {
    unsigned int i0 = indices[3 * i + 0];
    unsigned int i1 = indices[3 * i + 1];
    unsigned int i2 = indices[3 * i + 2];

    float3 p0(vertices[3 * i0 + 0], vertices[3 * i0 + 1], vertices[3 * i0 + 2]);
    float3 p1(vertices[3 * i1 + 0], vertices[3 * i1 + 1], vertices[3 * i1 + 2]);
    float3 p2(vertices[3 * i2 + 0], vertices[3 * i2 + 1], vertices[3 * i2 + 2]);
    float3 e1 = p1 - p0;
    float3 e2 = p2 - p0;
    float3 n = vcross(e1, e2);

    normals[3 * i0 + 0] += n[0];
    normals[3 * i0 + 1] += n[1];
    normals[3 * i0 + 2] += n[2];

    normals[3 * i1 + 0] += n[0];
    normals[3 * i1 + 1] += n[1];
    normals[3 * i1 + 2] += n[2];

    normals[3 * i2 + 0] += n[0];
    normals[3 * i2 + 1] += n[1];
    normals[3 * i2 + 2] += n[2];
  }

  for (size_t i = 0; i < normals.size() / 3; i++) {
    float3 n(normals[3 * i + 0], normals[3 * i + 1], normals[3 * i + 2]);
    n.normalize();
    normals[3 * i + 0] = n[0];
    normals[3 * i + 1] = n[1];
    normals[3 * i + 2] = n[2];
  }
}

